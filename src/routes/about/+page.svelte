        <h1>What on Earth is the Doughnut?…</h1>
        <p>Humanity’s 21st century challenge is to meet the needs of all within the means of the planet. 
            In other words, to ensure that no one falls short on life’s essentials 
            (from food and housing to healthcare and political voice), while ensuring that collectively we do not overshoot our pressure on Earth’s
             life-supporting systems, on which we fundamentally depend – such as a stable climate, fertile soils, 
             and a protective ozone layer. The Doughnut of social and planetary boundaries is a playfully serious approach to framing 
             that challenge, and it acts as a compass for human progress this century.</p>
